std::vector<TServerNickTableValueType>
CRecordServerNickTable::SelectDataFromSQL(int iUin, int iGroupID)
{
    char szBuffer[1024];
    snprintf(szBuffer, sizeof(szBuffer), "SELECT * FROM server_nick_t WHERE uid=%d AND group_id=%d;" , iUin, iGroupID);
    std::vector<TServerNickTableValueType> stRet;
    TServerNickTableValueType stValue;

    try {
        taf::TC_Mysql::MysqlData stData = s_pMysqlHandle->queryRecord(szBuffer);
        for (size_t i = 0; i < stData.size(); ++i)
        {
            stValue.m_iUid = ::atoi(stData[i]["uid"].c_str());
            stValue.m_iGroupID = ::atoi(stData[i]["group_id"].c_str());
            stValue.m_strNick = stData[i]["nick"];
            stRet.push_back(stValue);
        }
    } catch(const taf::TC_Mysql_Exception& stEx) {
    }
    return stRet;
}
